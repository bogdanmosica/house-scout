interface I18nContextValue {
  locale: Locale
  setLocale: (l: Locale) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window === 'undefined') return 'ro'
    const stored = localStorage.getItem(STORAGE_KEY) as Locale | null
    return stored === 'ro' || stored === 'en' ? stored : 'ro'
  })

  useEffect(() => {
    document.documentElement.lang = locale
  }, [locale])

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l)
    localStorage.setItem(STORAGE_KEY, l)
  }, [])

  const t = useCallback(
    (key: string): string => translations[locale][key] ?? translations.en[key] ?? key,
    [locale],
  )

  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>
}

export function useTranslation() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useTranslation must be inside LocaleProvider')
  return ctx
}

export function translateQuestion(q: Question, t: (key: string) => string): Question {
  const isLight = q.banks.includes('light')
  const textKey = isLight ? `q.${q.id}_l` : `q.${q.id}`
  return {
    ...q,
    text: t(textKey),
    sub: q.sub !== undefined ? t(`q.${q.id}.sub`) : undefined,
    low: q.low !== undefined ? t(`q.${q.id}.low`) : undefined,
    high: q.high !== undefined ? t(`q.${q.id}.high`) : undefined,
    yesLabel: q.yesLabel !== undefined ? t(`q.${q.id}.yes`) : undefined,
    noLabel: q.noLabel !== undefined ? t(`q.${q.id}.no`) : undefined,
    options: q.options?.map((o, i) => ({ ...o, label: t(`q.${q.id}.${i}`) })),
  }
}

export function translateDefs<T extends { id: string; name: string; desc: string }>(
  defs: T[],
  t: (key: string) => string,
  prefix: 'room' | 'insp',
): T[] {
  return defs.map((d) => ({ ...d, name: t(`${prefix}.${d.id}`), desc: t(`${prefix}.${d.id}.desc`) }))
}
